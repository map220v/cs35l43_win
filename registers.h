#define __AC(X,Y)	(X##Y)
#define _AC(X,Y)	__AC(X,Y)
#define _AT(T,X)	((T)(X))

#define _UL(x)		(_AC(x, UL))

#define UL(x)		(_UL(x))

#define BITS_PER_LONG sizeof(long)*8

#define __GENMASK(h, l) \
	(((~UL(0)) - (UL(1) << (l)) + 1) & \
	 (~UL(0) >> (BITS_PER_LONG - 1 - (h))))
#define GENMASK(h, l) __GENMASK(h, l)

